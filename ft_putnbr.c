#include "libft.h"

void	ft_putnbr(int n)
{
	char num;

	num = '0' + n;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0)
	{
		n = n * -1;
		ft_putchar('-');
	}
	if (n < 10)
		ft_putchar(num);
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
