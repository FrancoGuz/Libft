int	ft_atoi(const char *str)
{
	int i;
	int n;
	int flag = 1;
	i = 0;
	while (str[i] == '\0' || str[i] == '\t' || str[i] == '\n' || str[i] == 
			'\f' || str[i] == '\v' || str[i] == '\r' || str[i] == ' ')
		i++;
	n = 0;
	if (str[i] == '-')
	{
		flag = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	return (n * flag);
}
